    private void loadStaff() {
        DBcontext db = new DBcontext();
        ArrayList<Staff_entity> staffs = db.getStaff();
        Object[] columnNames = {"Mã", "Tên", "Ngày sinh", "Giới tính", "Địa chỉ", "Điện thoại"};
        Object[][] data = new Object[staffs.size()][columnNames.length];
        for (int i = 0; i < staffs.size(); i++) {
            Staff_entity d = staffs.get(i);
            data[i][0] = d.getId_nv();
            data[i][1] = d.getTen_nv();
            data[i][2] = d.getDate_nv();
            data[i][3] = d.isGt_nv();
            data[i][4] = d.getDiachi_nv();
            data[i][5] = d.getSdt_nv();

        }
        model = new DefaultTableModel(data, columnNames);
        tblNhanVien.setModel(model);
    }
    public void Search(String s){
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tblNhanVien.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(txtSearch.getText()));
    }
    

