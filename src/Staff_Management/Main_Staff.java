    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int index = tblNhanVien.getSelectedRow();
        if (index != -1) {
            txtID.setText((String) tblNhanVien.getModel().getValueAt(index, 0));
            txtName.setText((String) tblNhanVien.getModel().getValueAt(index, 1));
            txtDob.setText((String) tblNhanVien.getModel().getValueAt(index, 2).toString());
            rbnMale.setSelected((Boolean) tblNhanVien.getModel().getValueAt(index, 3));
            rbnFemale.setSelected(!(Boolean) tblNhanVien.getModel().getValueAt(index, 3));
            txtAddress.setText((String) tblNhanVien.getModel().getValueAt(index, 4));
            txtPhone.setText("" + tblNhanVien.getModel().getValueAt(index, 5));
            
            btnDel.setEnabled(true);
            btnEdit.setEnabled(true);
        }else{
            clear();
            btnDel.setEnabled(false);
            btnEdit.setEnabled(false);
        }
        

    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed

        String id = txtID.getText();
        String name = txtName.getText();
        Date dob = Date.valueOf(txtDob.getText());
        Boolean gender;
        while (true) {
            if (rbnFemale.isSelected()) {
                gender = false;
                break;
            }
            if (rbnMale.isSelected()) {
                gender = true;
                break;
            }
        }
        String address = txtAddress.getText();
        int phone = Integer.parseInt(txtPhone.getText());
        Staff_entity staff = new Staff_entity(id, name, dob, gender, address, phone);
        db.insert(staff);
        loadStaff();
        clear();
        btnDel.setEnabled(false);
        btnEdit.setEnabled(false);

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        staffs = db.getStaff(txtID.getText());
        staffs.setTen_nv(txtName.getText());
        staffs.setDate_nv(Date.valueOf(txtDob.getText()));
        staffs.setGt_nv(rbnMale.isSelected());
        staffs.setDiachi_nv(txtAddress.getText());
        staffs.setSdt_nv(Integer.parseInt(txtPhone.getText()));
        db.update(staffs);
        loadStaff();
        clear();
        btnDel.setEnabled(false);
        btnEdit.setEnabled(false);

    }//GEN-LAST:event_btnEditActionPerformed

