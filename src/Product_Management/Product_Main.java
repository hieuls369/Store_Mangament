    private void loadCate() {
        DBcontext db = new DBcontext();
        ArrayList<Product_Cate> cate = db.getCate();
        Product_Cate[] iCate = new Product_Cate[cate.size()];
        cate.toArray(iCate).toString();

        cbxModel = new DefaultComboBoxModel<>(iCate);
        cbLoai_sp.setModel(cbxModel);
    }

    private void loadProduct() {
        DBcontext db = new DBcontext();
        ArrayList<Product_entity> products = db.getProducts();
        Object[] columnNames = {"Mã", "Loại", "Tên", "Hãng", "Bảo hành", "Giá", "Số lượng còn", "Đường dẫn"};
        Object[][] data = new Object[products.size()][columnNames.length];
        for (int i = 0; i < products.size(); i++) {
            Product_entity p = products.get(i);
            data[i][0] = p.getMa_sp();
            data[i][1] = p.getLoai_sp();
            data[i][2] = p.getTen_sp();
            data[i][3] = p.getHang_sp();
            data[i][4] = p.getBao_hanh();
            data[i][5] = p.getGia_sp();
            data[i][6] = p.getSlg_sp();
            data[i][7] = p.getDuong_dan();

        }
        model = new DefaultTableModel(data, columnNames);
        tbSP.setModel(model);
    }

