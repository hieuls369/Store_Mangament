    private void btnPictureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPictureActionPerformed
        
        JFileChooser j = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        j.setAcceptAllFileFilterUsed(false);

        j.setDialogTitle("Choose a picture");
        
        FileNameExtensionFilter restrict = new FileNameExtensionFilter("Picture only", "jpg", "png");
        j.addChoosableFileFilter(restrict);

        int r = j.showOpenDialog(null);

        if (r == JFileChooser.APPROVE_OPTION) {
            txtDD.setText(j.getSelectedFile().getPath());

        }
        loadPicture(txtDD.getText().replaceAll("\\\\", "/"));

    }//GEN-LAST:event_btnPictureActionPerformed

