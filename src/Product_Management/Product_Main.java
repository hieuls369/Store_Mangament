    private void tbSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSPMouseClicked
        int index = tbSP.getSelectedRow();
        if (index != -1) {
            txtMa_sp.setText((String) tbSP.getModel().getValueAt(index, 0));

            cbLoai_sp.getModel().setSelectedItem((String) tbSP.getModel().getValueAt(index, 1));

            txtTen_sp.setText((String) tbSP.getModel().getValueAt(index, 2));
            txtHang.setText((String) tbSP.getModel().getValueAt(index, 3));
            txtBao_hanh.setText((String) tbSP.getModel().getValueAt(index, 4));
            txtGia_sp.setText((String) tbSP.getModel().getValueAt(index, 5));
            txtSlg_sp.setText("" + tbSP.getModel().getValueAt(index, 6));
            txtDD.setText((String) tbSP.getModel().getValueAt(index, 7));
            loadPicture(txtDD.getText().replaceAll("\\\\", "/"));
//            txtMa_sp.setEnabled(false);
            btnDel.setEnabled(true);
            btnEdit.setEnabled(true);

        } else {
            clear();
            btnDel.setEnabled(false);
            btnEdit.setEnabled(false);

        }
    }//GEN-LAST:event_tbSPMouseClicked

